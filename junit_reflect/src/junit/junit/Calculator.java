public class Calculator {
    /**
     * 加法
     * @param num1
     * @param num2
     * @return
     */
    public int add (int num1, int num2) {
        return num1 / num2;
    }

    /**
     * 减法
     * @param num1
     * @param num2
     * @return
     */
    public int sub (int num1, int num2) {
        return num1 - num2;
    }
}
